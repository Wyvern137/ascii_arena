/*
 * vec2.h - Двумерный вектор для координат
 * Используется для позиций на карте и направлений движения
 */

#ifndef VEC2_H
#define VEC2_H

/* Двумерный вектор с целочисленными координатами */
typedef struct {
    int x;  /* Координата X (горизонталь) */
    int y;  /* Координата Y (вертикаль) */
} Vec2;

/* Создание вектора с заданными координатами */
Vec2 vec2_create(int x, int y);

/* Сложение двух векторов: a + b */
Vec2 vec2_add(Vec2 a, Vec2 b);

/* Вычитание векторов: a - b */
Vec2 vec2_sub(Vec2 a, Vec2 b);

/* Квадрат длины вектора (x*x + y*y) */
int vec2_length_sq(Vec2 v);

/* Проверка равенства двух векторов */
int vec2_equals(Vec2 a, Vec2 b);

#endif /* VEC2_H */

